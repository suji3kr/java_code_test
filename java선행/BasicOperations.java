public class BasicOperations {
    public static void main(String[] args) {
        int a = 13;
        int b = 5;
        System.out.println("합계:" +(a+b));
        System.out.println("빼기:" +(a-b));
        System.out.println("곱셈:" +(a*b));
        System.out.println("나눗셈:" +(a/b));
        System.out.println("나머지:" +(a%b));
    }
}
